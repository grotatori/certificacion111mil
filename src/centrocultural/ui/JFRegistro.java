    private int determinarMes(){
        switch(this.cbMes.getSelectedItem().toString()){
            case "Enero":  return 0;
            case "Febrero": return 1;
            case "Marzo": return 2;
            case "Abril": return 3;
            case "Mayo": return 4;
            case "Junio": return 5;
            case "Julio": return 6;
            case "Agosto": return 7;
            case "Septiembre": return 8;
            case "Octubre": return 9;
            case "Noviembre": return 10;
            case "Diciembre": return 11; 
        }
        return 0;
    }
    
    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        if(this.verificarCampos()){
            Calendar fechaNac = generarFecha();
            if(determinarEdad(fechaNac) && "".equals(txtPadreTutor.getText())){
                lbPadreMadreTutor.setVisible(true);
                txtPadreTutor.setVisible(true);
                lbMensaje.setText("Es menor de edad, complete el campo extra");
            }else{
                try{
                    ctrlAlumnos.nuevoParticipante(txtApellido.getText(), txtNombre.getText(), Long.parseLong(txtTelefono.getText()), txtEmail.getText(), fechaNac.getTime(), txtPadreTutor.getText());
                    lbMensaje.setText("Participante agregado con exito!");

                    System.out.println(this.ctrlAlumnos.getParticipante(this.txtEmail.getText()));
                }catch(ExistException ee){
                    lbMensaje.setText(ee.getMessage());
                }
                limpiarCampos();
            }
        }else{
            lbMensaje.setText("Campos incompletos");
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

    private Calendar generarFecha(){
        int dia = Integer.parseInt(this.cbDia.getSelectedItem().toString());
        int mes = determinarMes();
        int anio = Integer.parseInt(this.cbAnio.getSelectedItem().toString());
        Calendar calendar = Calendar.getInstance();
        calendar.set(anio, mes, dia);
        return calendar;
    }
    
    private boolean determinarEdad(Calendar fechaNac){
        Calendar hoy = Calendar.getInstance();
        
        int difAnio = hoy.get(Calendar.YEAR) - fechaNac.get(Calendar.YEAR);
        int difMes = hoy.get(Calendar.MONTH) - fechaNac.get(Calendar.MONTH);
        int difDia = hoy.get(Calendar.DAY_OF_MONTH) - fechaNac.get(Calendar.DAY_OF_MONTH);
        
        if(difMes < 0 || (difMes == 0 && difDia < 0))
            difAnio--;
        
        return difAnio < 18;
    }
    
