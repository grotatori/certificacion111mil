    private void cargarCBAnio(){
        for(int i = 1920; i<=2018; i++)
            cbAnio.addItem(String.valueOf(i));
    }

    private boolean verificarCampos(){
        boolean aux = txtPadreTutor.isVisible() && !"".equals(txtPadreTutor.getText()) || !txtPadreTutor.isVisible();
        return !"".equals(txtNombre.getText()) && !"".equals(txtApellido.getText()) && !"".equals(txtEmail.getText()) && !"".equals(txtTelefono.getText()) && aux;
    }
    
    private void limpiarCampos(){
        txtApellido.setText("");
        txtNombre.setText("");
        txtEmail.setText("");
        txtTelefono.setText("");
        txtPadreTutor.setText("");
        lbPadreMadreTutor.setVisible(false);
        txtPadreTutor.setVisible(false);
        
        cbDia.setSelectedIndex(0);
        cbMes.setSelectedIndex(0);
        cbAnio.setSelectedIndex(0);
    }

