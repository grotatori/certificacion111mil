    private void cargarTabla(){
        this.jtCursos.removeAll();
        List<Curso> lista = ctrlCurso.getCursosByTematica(cbTematica.getSelectedItem().toString()); //ctrlCurso.getAllCursos();
        Object[][] data = new Object[lista.size()][6];
        String[] columnNames = {"Nombre", "Profesor", "Aula", "Duracion", "Carga Horaria", "Cupos"};
        cursoIds.clear();
        for(int i = 0; i<lista.size(); i++){
            Curso aux = lista.get(i);
            cursoIds.add(aux.getId());
            data[i][0] = aux.getNombre();
            data[i][1] = aux.getProfesor().getNombre()+ " " + aux.getProfesor().getApellido();
            data[i][2] = aux.getAula();
            data[i][3] = aux.getDuracion() + " meses";
            data[i][4] = aux.getCargaHoraria() + " hs/semana";
            data[i][5] = aux.getCuposDisponibles();
        }
        DefaultTableModel modelo = new DefaultTableModel(data, columnNames){
                @Override
                public boolean isCellEditable(int row, int column) {
                   return false;
                }
        };
        this.jtCursos.setModel(modelo);
    }
