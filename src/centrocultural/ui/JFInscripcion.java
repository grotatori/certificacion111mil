    private void btnInscribirseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInscribirseActionPerformed
        // TODO add your handling code here:
        int fila = jtCursos.getSelectedRow();
        if(fila != -1){
            Curso curso = ctrlCurso.getCursoById(cursoIds.get(fila));
            try{
                int condicion = ctrlInscripcion.nuevaInscripcion(curso, participante);
                cargarTabla();
                String mensaje;
                if(condicion == 1)
                    mensaje = "Inscripcion al curso completada";
                else
                    mensaje = "Su incripcion ha sido condicional, debe esperar a que se";
                JOptionPane.showMessageDialog(this, "Inscripcion al curso completada");
            }catch(ExistException ee){
                lbMensaje.setText(ee.getMessage());
            }
        }else{
            lbMensaje.setText("Debe seleccionar un curso");
        }
        
    }//GEN-LAST:event_btnInscribirseActionPerformed

